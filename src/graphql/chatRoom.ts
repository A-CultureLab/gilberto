import { gql } from "@apollo/client";
import { createLazyQueryHook, createMutationHook, createQueryHook } from "../lib/createApolloHook";
import { chatRooms, chatRoomsVariables } from "./__generated__/chatRooms";


//--------------------------------------------------------------------------------------------------------------------------------------------------------//
export const CHAT_ROOMS = gql`
query chatRooms ($where:ChatRoomWhereInput, $skip:Int, $take:Int) {
    chatRooms(where:$where, skip:$skip, take:$take) {
        id
        notReadChatCount
        users {
            id
            name,
            image
        }    
        recentChat {
            id
            createdAt
            message
        }
    }
}
`
export const useChatRooms = createQueryHook<chatRooms, chatRoomsVariables>(CHAT_ROOMS)
